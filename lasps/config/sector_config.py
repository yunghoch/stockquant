# 13개 섹터 구조 (v3: 20개 → 13개 병합)
SECTOR_NAMES = {
    0: "전기전자",
    1: "금융업",
    2: "서비스업",
    3: "의약품",
    4: "유통업",
    5: "철강금속",
    6: "기계",
    7: "화학",
    8: "건설업",
    9: "음식료품",
    10: "운수",       # 운수장비 + 운수창고 병합
    11: "제조업",     # KOSDAQ 제조
    12: "기타",       # 섬유의복, 비금속광물, 종이목재, 통신업, 전기가스 등
}

NUM_SECTORS = 13
DEFAULT_SECTOR_ID = 12  # 기타 - 미매핑 업종코드의 기본값

# 기존 20개 섹터 → 13개 섹터 매핑 (학습 데이터 변환용)
OLD_TO_NEW_SECTOR = {
    0: 0,   # 전기전자 → 전기전자
    1: 1,   # 금융업 → 금융업
    2: 2,   # 서비스업 → 서비스업
    3: 3,   # 의약품 → 의약품
    4: 10,  # 운수장비 → 운수
    5: 4,   # 유통업 → 유통업
    6: 5,   # 철강금속 → 철강금속
    7: 6,   # 기계 → 기계
    8: 7,   # 화학 → 화학
    9: 8,   # 건설업 → 건설업
    10: 12, # 섬유의복 → 기타
    11: 9,  # 음식료품 → 음식료품
    12: 12, # 비금속광물 → 기타
    13: 12, # 종이목재 → 기타
    14: 10, # 운수창고 → 운수
    15: 12, # 통신업 → 기타
    16: 12, # 전기가스 → 기타
    17: 11, # 제조업(기타) → 제조업
    18: 12, # 농업임업어업 → 기타
    19: 12, # 광업 → 기타
}


def get_sector_name(sector_id: int) -> str:
    """섹터 ID로 섹터명 조회."""
    return SECTOR_NAMES.get(sector_id, "Unknown")


def convert_old_to_new_sector(old_sector_id: int) -> int:
    """기존 20개 섹터 ID를 새 13개 섹터 ID로 변환."""
    return OLD_TO_NEW_SECTOR.get(old_sector_id, DEFAULT_SECTOR_ID)
